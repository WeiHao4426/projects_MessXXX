% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
global x filepath filename
[filename,filepath]=uigetfile({'*.bmp;*.jpg'},'选择图片文件');
 if isequal(filename,0)
     errordlg('您未选择图片','温馨提示');
 else
     set(handles.edit3,'string',[filepath filename]);
     x = imread([filepath filename]);
     axes(handles.axes1);
     imshow(x);
     set(handles.axes1,'xtick',[],'ytick',[]);
     set(handles.text7,'string',GetDMax([filepath filename]));
 end
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)


% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
global FilterType filepath filename n D0
filtertype = string(FilterType);
switch filtertype
    case 'ILPF'
        ProcessedPic = ILPF([filepath filename],D0);
    case 'BLPF'
        ProcessedPic = BLPF([filepath filename],D0,n);
    case 'GLPF'
        ProcessedPic = GLPF([filepath filename],D0);
end
axes(handles.axes2);
imshow(ProcessedPic,[]);
set(handles.axes2,'xtick',[],'ytick',[]);
